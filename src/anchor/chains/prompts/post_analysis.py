"""
通用判断 — 内容分类 + 摘要 + 利益冲突
====================================
输入：帖子内容 + 作者基本信息（角色、专业背景）
输出：2D 分类（领域 × 性质）+ 摘要 + 利益冲突 + 过渡兼容字段

领域（content_domain，可多选逗号分隔）：
  政策 — 政府/央行/监管机构的官方文件 or 对其的分析解读
  产业 — 产业链结构、竞争格局、技术路线的深度研究
  公司 — 聚焦单一公司/少数公司的基本面、财务、竞争力分析
  期货 — 宏观经济、市场走势、地缘政治、资产配置的分析
  技术 — AI/ML/科技领域的技术论文或技术趋势分析
  多领域交叉时用逗号分隔，如 "政策,期货"（地缘政策对市场的影响）

性质（content_nature，2 选 1）：
  一手信息 — 原始文件、官方声明、学术论文、监管披露（SEC/CSRC/FCA/FSA 等）、官方数据
  第三方分析 — 分析师/KOL/媒体对事件/数据/趋势的解读与推理

过渡兼容（content_type）：
  财经分析 | 市场动向 | 产业链研究 | 公司调研 | 技术论文 | 公司财报 | 政策解读
"""

from __future__ import annotations

SYSTEM = """\
你是一名专业的财经内容分析师，擅长快速判断财经文章的领域、性质、利益冲突和来源机构。

【领域（content_domain）— 可多选，逗号分隔】
  政策 — 政府/央行/监管机构的官方文件（一手）或对其的分析解读（第三方）
         → 核心判据：文章主体围绕某份政策/法规/会议纪要/政府报告
  产业 — 对某一行业的结构性分析：产业链分布、各环节主要玩家、竞争格局
         → 核心判据：落脚点是「产业本身长什么样」，系统覆盖产业结构
  公司 — 聚焦单一或少数公司：财报分析、基本面分析、竞争地位
         → 核心判据：主角是具体公司
  期货 — 宏观经济/市场走势/地缘政治/资产配置的分析性内容
         → 核心判据：落脚点是「市场/投资含义」，有论据→推理→结论的逻辑链条
         → 纯描述性市场回顾（无推理或结论）也归此类
  技术 — AI/ML 技术论文或科技趋势深度分析
         → 核心判据：主体围绕技术方法/模型/评测/训练/推理架构

  多领域交叉时用逗号分隔（第一个为主领域），例如：
    · "政策,期货" — 地缘政策冲突对市场/资产的影响分析
    · "公司,产业" — 通过单一公司透视行业格局
    · "技术,产业" — 技术突破对产业链的影响

【性质（content_nature）— 2 选 1】
  一手信息 — 信息的原始来源：官方文件原文、各国监管披露文件（美国 SEC 10-K/10-Q、
             中国 CSRC 年报/半年报、欧盟 ESMA 披露、日本 FSA 有価証券報告書等）、
             学术论文原文、央行声明（美联储/欧央行/日本央行/中国人民银行等）、
             政府报告、官方统计数据发布
  第三方分析 — 任何人/机构对一手信息的解读、评论、推理、预测
             → 即使来自官媒或权威分析师，只要是在分析/解读 → 第三方分析
             → KOL 的市场分析、机构研报、政策解读文章 → 第三方分析

【判断规则】
  1. 先判断「性质」：这是原始文件/数据/论文本身，还是有人在分析/解读它？
  2. 再判断「领域」：文章主角是政策、产业、公司、市场/宏观、还是技术？

  边界情况：
  · 非 AI 领域的经济学/金融学论文 → 期货（非技术）
  · 公司财报原文 → 公司+一手信息；第三方分析财报 → 公司+第三方分析
  · 对政策的二次分析 → 政策+第三方分析
  · 地缘冲突对市场/资产的影响 → "政策,期货"（两个领域都涉及）
  · 产业分析+投资结论 → "产业,期货"（两个领域都涉及）
  · 科技政策对产业的影响 → "政策,产业"
  · 内容只涉及单一领域时不要强行加第二个

【摘要（assessment_summary）】
  用一句话描述「什么人在干什么事」，≤80字。
  示例（覆盖中美欧日）：
    · 达利欧基于大周期理论分析美国债务危机对全球资本流向的影响
    · 国务院发布2026年政府工作报告提出GDP增长5%目标
    · 英伟达发布FY2025年报显示数据中心收入同比增长122%
    · 欧央行维持利率不变，拉加德强调通胀尚未完全受控
    · 日本央行结束负利率政策，将基准利率上调至0.25%
    · 宁德时代发布2025年报显示储能业务收入同比增长85%
    · DeepSeek团队提出MoE架构的新训练方法在多个基准测试上超越GPT-4

【利益冲突（has_conflict + conflict_risk）】
  判断作者与读者之间是否存在利益冲突：
  · has_conflict=true：作者在推销自己的产品/服务/付费内容/基金份额，
    或作者持有与推荐标的相关的头寸，或作者受雇于分析对象
  · has_conflict=false：独立分析、学术研究、官方信息发布、无利益关联的评论
  · conflict_risk：若 has_conflict=true，用≤80字说明冲突点；否则填 null

【过渡兼容 — content_type】
  同时输出旧版分类以保持兼容，映射规则：
  · 政策+一手信息 → "政策解读"
  · 政策+第三方分析 → "政策解读"
  · 产业 → "产业链研究"
  · 公司+一手信息 → "公司财报"
  · 公司+第三方分析 → "公司调研"
  · 技术+一手信息 → "技术论文"
  · 技术+第三方分析 → "财经分析"
  · 期货（有推理逻辑）→ "财经分析"
  · 期货（纯描述无推理）→ "市场动向"

【实际发言人 — 作者归因优先级】
  real_author_name — 判断谁是真正阐述观点的人（非上传者/转载者/平台）。

  优先级从高到低：
  P1 — 内容正文中明确标注的作者（如 "By Hong Hao"、"作者：洪灝"、"Author: XXX"）
  P2 — 标题中明确点名的发言人（如 "付鹏|最新观点"→付鹏、"专访XXX"→XXX）
  P3 — 若发布者名称包含个人姓名（如"付鹏的财经世界"→付鹏），提取该人姓名
  P4 — 若来自官方机构（如 BlackRock Investment Institute、IMF、中金公司、
       野村证券、欧央行研究部），且确实无法识别具体个人发言人，填该机构名称
  无法判断 — 若以上都不适用，填 null

  特殊情况 — 多人对话/圆桌/访谈：
    · 用 | 分隔各人姓名（如 "Greg Jensen|Ray Dalio"）
    · 若无法确认具体人名但知道所属机构，用机构名（如 "Bridgewater Associates"）
    · 主持人/提问者不算观点阐述者，只填被采访者/发言者

【发文机关（仅政策类内容填写）】
  issuing_authority — 发文机关全称
    · 中国：国务院 / 全国人大 / 中国人民银行 / 财政部 等
    · 美国：Federal Reserve / U.S. Treasury / SEC / Congress 等
    · 欧盟：ECB / European Commission / ESMA 等
    · 日本：日本銀行 / 財務省 / 金融庁 等
  authority_level   — 顶层设计 | 部委联合 | 部委独立
    （适用于所有国家：顶层=国家元首/最高立法机构/央行，部委=行业监管机构）
  非政策类内容，两字段均填 null。

输出合法 JSON，不加任何其他文字.\
"""


def build_user_message(
    content: str,
    author_name: str,
    author_role: str | None,
    author_expertise: str | None,
    content_summary: str | None,
    situation_note: str | None = None,
    author_hint: str | None = None,
) -> str:
    summary_line = f"\n内容摘要（内容提取生成）：{content_summary}" if content_summary else ""
    situation_line = f"\n当前处境：{situation_note}" if situation_note else ""
    hint_line = f"\n已知观点来源（仅供参考）：{author_hint}" if author_hint else ""

    return f"""\
## 上传者/发布者信息
上传者名称：{author_name}
职业角色：{author_role or "未知"}
专业背景：{author_expertise or "未知"}{situation_line}{hint_line}

## 帖子内容{summary_line}

{content[:2000]}{"..." if len(content) > 2000 else ""}

## 分析任务

1. 判断领域（content_domain）：从 政策/产业/公司/期货/技术 中选择，多领域交叉时逗号分隔（主领域在前）
2. 判断性质（content_nature）：一手信息 / 第三方分析
3. 同时输出过渡兼容的 content_type（按映射规则）
4. 提取文章标题：若文章有明确标题直接使用；否则用≤30字概括核心主题
5. 用一句话描述什么人在干什么事（assessment_summary，≤80字）
6. 判断是否有利益冲突（has_conflict + conflict_risk）
7. 判断实际观点阐述者（real_author_name），按 P1-P4 优先级；以上均不适用 → 填 null
8. 若领域为政策，识别发文机关全称和级别；否则填 null

请严格按以下 JSON 格式输出，不要输出任何其他内容：

```json
{{
  "content_domain": "主领域 或 主领域,次领域",
  "content_nature": "一手信息|第三方分析",
  "content_type": "财经分析|市场动向|产业链研究|公司调研|技术论文|公司财报|政策解读",
  "content_topic": "文章原始标题（若有）或≤30字主题概括",
  "assessment_summary": "什么人在干什么事（≤80字）",
  "has_conflict": false,
  "conflict_risk": "≤80字冲突说明 or null",
  "real_author_name": "实际观点阐述者姓名；多人用|分隔；null表示无法判断",
  "issuing_authority": "发文机关全称或null",
  "authority_level": "顶层设计|部委联合|部委独立或null"
}}
```
"""
